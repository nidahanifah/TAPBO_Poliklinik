   private void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id_Pasien");
        model.addColumn("Nama");
        model.addColumn("NIK");
        model.addColumn("Tanggal Lahir");
        model.addColumn("Jenis Kelamin");
        model.addColumn("Golongan Darah");
        model.addColumn("Jenis Pelayanan");
        model.addColumn("No Telpon");
        model.addColumn("Nama Wali");
        model.addColumn("Alamat");
        
        try{
            
            String sql = "select * from pasien";
            java.sql.Connection conn = (java.sql.Connection)convig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while  (res.next()){
               model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3), res.getString(4), res.getString(5)
                , res.getString(6), res.getString(7), res.getString(8), res.getString(9), res.getString(10)});
            }
            tPasien.setModel(model);
        }catch (SQLException exp) {
            JOptionPane.showMessageDialog(null, exp.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }     
    }
