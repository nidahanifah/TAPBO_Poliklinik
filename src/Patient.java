    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String id_pasien = jTextField1.getText();
        String nama = jTextField2.getText();
        String nik = jTextField3.getText();
        String tgl = new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser1.getDate());
        String JenKel;
        if (rdlaki.isSelected()){
           JenKel = rdlaki.getText();
        }else{
           JenKel = rdpere.getText();
        }
        
        String GolDar;
        if (gdA.isSelected()){
           GolDar = gdA.getText();
        }else if (gdB.isSelected()){
           GolDar = gdB.getText();
        }else if (gdAB.isSelected()){
           GolDar = gdAB.getText();
        }else{
            GolDar = gdO.getText();
        }
        String JenPe = (String)JP.getSelectedItem();
        String no_telp = jTextField13.getText();
        String wali = jTextField14.getText();
        String alamat = jTextArea1.getText();
        
        try{
            if (!id_pasien.isEmpty() && !nama.isEmpty() && !nik.isEmpty() && !tgl.isEmpty()
                && !JenKel.isEmpty() && !GolDar.isEmpty() && !JenPe.isEmpty() && !no_telp.isEmpty()
                && !wali.isEmpty() && !alamat.isEmpty()) {
                
                String sql= "INSERT INTO pasien VALUES "
                            + "('"+id_pasien+"','"+nama+"','"
                            +nik+"','"+tgl+"','"+JenKel+"','"
                            +GolDar+"','"+JenPe+"','"+no_telp
                            +"','"+wali+"','"+alamat+"')";
                java.sql.Connection conn=(java.sql.Connection)convig.configDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Penyimpanan Berhasil", "Success", JOptionPane.INFORMATION_MESSAGE);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jDateChooser1.setCalendar(null);
                jTextField13.setText("");
                jTextField14.setText("");
                jTextArea1.setText("");
                GD.clearSelection();
                JK.clearSelection();
                JP.setSelectedIndex(0);
            }else{
                JOptionPane.showMessageDialog(null, "Data yang Diinputkan Kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }catch (SQLException exp) {
            JOptionPane.showMessageDialog(null, exp.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

